// Function to get all electives relevant for a specific semester
export const getRelevantElectivesForSemester = (semester: string) => {
  const semesterNum = parseInt(semester);
  
  if (semesterNum <= 5) {
    return {};
  }
  
  // Map semester to relevant electives
  if (semesterNum === 6) {
    return {
      professional: {
        "PE2": professionalElectives["PE2"],
        "PE3": professionalElectives["PE3"],
        "PE2LAB": professionalElectives["PE2LAB"],
      },
      open: {
        "OE1": openElectives["OE1"]
      }
    };
  }
  
  if (semesterNum === 7) {
    return {
      professional: {
        "PE4": professionalElectives["PE4"],
        "PE5": professionalElectives["PE5"]
      },
      open: {
        "OE2": openElectives["OE2"]
      }
    };
  }
  
  if (semesterNum === 8) {
    return {
      professional: {
        "PE6": professionalElectives["PE6"]
      },
      open: {
        "OE3": openElectives["OE3"]
      }
    };
  }
  
  return {};
};
